function wishMessage(message) {
  if (message === "GM") {
    console.log("Good Morning Geeks");
  } else if (message === "GE") {
    console.log("Good Evening Geeks"); //switch
  } else if (message === "GA") {
    console.log("good afternoon Geeks");
  } else {
    console.log("Good night Geeks");
  }
}

wishMessage("GA");
